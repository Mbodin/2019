function lodiv(element){
  if($("#lotext").text().length == 0) {
    /* si le programme n'est pas encore chargé, l'afficher */
    $("#lotext").load("programmes/lo.html");
    $(element).text('Fermer');
  }
  else {
    /* sinon, le masquer */
    $("#lotext").empty();
    $(element).text('Dérouler')
  }
};

